namespace ShadcnBlazor.Components.Input.InputGroup;

/// <summary>
/// Alignment position of an input group addon.
/// </summary>
public enum InputGroupAddonAlign
{
    /// <summary>Align at the start of the inline axis (usually left).</summary>
    InlineStart,
    /// <summary>Align at the end of the inline axis (usually right).</summary>
    InlineEnd,
    /// <summary>Align at the start of the block axis (usually top).</summary>
    BlockStart,
    /// <summary>Align at the end of the block axis (usually bottom).</summary>
    BlockEnd
}

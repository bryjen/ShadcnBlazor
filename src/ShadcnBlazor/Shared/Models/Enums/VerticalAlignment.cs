/// Vertical alignment of a control (e.g. checkbox, radio) relative to its label.
/// </summary>
public enum VerticalAlignment
{
    /// <summary>Align to the top of the label.</summary>
    Top,

    /// <summary>Align to the center of the label (default).</summary>
    Center,

    /// <summary>Align to the bottom of the label.</summary>
    Bottom,
}
